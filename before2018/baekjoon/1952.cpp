#include <stdio.h>
int main(){
	int a[102][102]={0};
	int n,m,i,j,count=0,q=0,x=1,y=1,p=0;
	scanf("%d%d",&m,&n);
	for(i=1;i<m+1;i++){
		for(j=1;j<n+1;j++){
			a[i][j]=1;
		}
	}
	a[1][1]=0;
	while(1){
		while(1){
			if(a[y][x+1]==1){
				a[y][++x]=0;
				q++;
				if(q==m*n-1){
					p=1;
					break;
				}
			}
			else{
				count++;
				break;
			}
		}
		if(p==1){
			break;
		}
		while(1){
			if(a[y+1][x]==1){
				a[++y][x]=0;
				q++;
				if(q==m*n-1){
					p=1;
					break;
				}
			}
			else{
				count++;
				break;
			}
		}
		if(p==1){
			break;
		}
		while(1){
			if(a[y][x-1]==1){
				a[y][x--]=0;
				q++;
				if(q==m*n-1){
					p=1;
					break;
				}
			}
			else{
				count++;
				break;
			}
		}
		if(p==1){
			break;
		}
		while(1){
			if(a[y-1][x]==1){
				a[y--][x]=0;
				q++;
				if(q==m*n-1){
					p=1;
					break;
				}
			}
			else{
				count++;
				break;
			}
		}
		if(p==1){
			break;
		}
	}
	printf("%d",count);
}
